# 3. Работа с возрастом абитуриента

age = int(input("Введите ваш возраст: "))
name = input("Введите ваше имя: ")

# а) Проверка диапазона возраста
if age <= 0 or age >= 75:
    print("Некорректный возраст")
else:
    # б) Проверка имени
    if name == "Иван":
        print("Поступающего зовут Иван — отказано в поступлении.")
    else:
        # в) Основная логика
        if age >= 16:
            print("Поздравляем вы поступили в ВГУИТ")
        else:
            print("Сначала нужно окончить школу!")
            print(f"Вам нужно учиться еще {16 - age} лет")


# 4. Перевод секунд в дни:часы:минуты:секунды

seconds = int(input("Введите количество секунд: "))

days = seconds // (24 * 3600)
seconds %= (24 * 3600)

hours = seconds // 3600
seconds %= 3600

minutes = seconds // 60
seconds %= 60

print(f"{days}:{hours:02}:{minutes:02}:{seconds:02}")


