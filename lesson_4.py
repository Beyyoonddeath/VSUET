# 7. 
# По данному натуральном n вычислите сумму 1!+2!+3!+...+n!. В решении 
# этой задачи можно использовать только один цикл. Пользоваться математической 
# библиотекой math в этой задаче запрещено. 

n = 5
factorial = 1
total = 0

for i in range(1, n + 1):
    factorial *= i
    total += factorial

print(total)
    
# 8. 
# По данному натуральному n ≤ 9 выведите лесенку из n ступенек, i-я 
# ступенька состоит из чисел от 1 до i без пробелов. 

n = int(input())
for i in range(1, n + 1):
    print(''.join(str(j) for j in range(1, i + 1)))

# 9. 
# Пользователь вводит число N с клавиатуры - количество чисел из ряда 
# Фибоначчи. Посчитайте сумму этих чисел. 

n = int(input())

a, b = 0, 1
total = 0

for _ in range(n):
    total += b
    a, b = b, a + b

print(total)

# 10. 
# Пользователь вводит два числа N и K с клавиатуры. N - количество чисел из 
# ряда Фибоначчи, K - порядковый номер в ряду, с которого нужно начать. Посчитайте 
# сумму этих чисел. В решении этой задачи можно использовать только один цикл.

n = int(input())
k = int(input())

a, b = 0, 1
total = 0

for i in range(1, k + n):
    current = b
    a, b = b, a + b
    if i >= k:
        total += current

print(total)
