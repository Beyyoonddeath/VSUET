# 6. Поменять значения переменных x и y местами

x = 10
y = 20

print(f"До: x = {x}, y = {y}")

# Способ 1: через временную переменную
temp = x
x = y
y = temp

print(f"После (через temp): x = {x}, y = {y}")

# Способ 2: Синтаксис Python
x, y = y, x

print(f"После (обмен через кортеж): x = {x}, y = {y}")

