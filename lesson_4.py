# 2. 
# Даны два целых числа A и В. Выведите все числа от A до B включительно, в 
# порядке возрастания, если A < B, или в порядке убывания в противном случае. 

a = 2
b = 7

if a < b:
    c = a
    while c <= b:
        print(c)
        c += 1
else:
    c = b
    while c >= a:
        print(c)
        c -= 1

